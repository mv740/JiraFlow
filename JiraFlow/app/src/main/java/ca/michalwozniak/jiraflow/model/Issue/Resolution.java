public class Resolution {
    private String self;
    private String id;
    private String description;
    private String name;

    public String getSelf() {
        return self;
    }

    public String getId() {
        return id;
    }

    public String getDescription() {
        return description;
    }

    public String getName() {
        return name;
    }
}
